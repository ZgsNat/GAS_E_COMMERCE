    @Override
    protected void doGet(HttpServletRequest req, HttpServletResponse resp)
            throws ServletException, IOException {
         HttpSession session = req.getSession();
        if (session.getAttribute("Customer") == null) {
            req.getRequestDispatcher("Client/login.jsp").forward(req, resp);
            return;
        }
        String CusID;
        try {
            CusID = session.getAttribute("ID").toString();
        } catch (Exception e) {
            CusID = "";
        }
        Orders o = new Orders();
        Customer c = new Customer();
        c = c.getCustomerByID(CusID);
        List<Orders> olist = o.getListOrderBycID(CusID);
        req.setAttribute("orders", olist);
        req.getRequestDispatcher("Client/PurchaseHistory.jsp").forward(req, resp);
    }

